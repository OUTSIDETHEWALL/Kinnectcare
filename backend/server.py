@api_router.delete("/members/{member_id}")
async def delete_member(member_id: str, current=Depends(get_current_user)):
    """Delete a family member AND every document that references them.

    v1.1.7 cascade — no orphaned data is allowed to remain.  Deletes from:
      • members
      • reminders            (medications + routines)
      • checkins
      • alerts               (SOS history, missed_checkin, medication, etc.)
      • medication_logs
      • med_notifications    (per-slot scheduler bookkeeping)
      • refill_notifications (refill-cycle bookkeeping)
    All scoped to the caller's family_group_id so a user can never
    accidentally (or deliberately) wipe another family's data.
    """
    fgid = current["family_group_id"]
    r = await db.members.delete_one({"id": member_id, "family_group_id": fgid})
    if r.deleted_count == 0:
        raise HTTPException(status_code=404, detail="Member not found")
    # Cascade — best-effort per collection, log any individual failures
    # but never abort the whole request (we already deleted the member).
    cascade_counts: dict = {}
    for coll, query in (
        ("reminders",            {"family_group_id": fgid, "member_id": member_id}),
        ("checkins",             {"family_group_id": fgid, "member_id": member_id}),
        ("alerts",               {"family_group_id": fgid, "member_id": member_id}),
        ("medication_logs",      {"family_group_id": fgid, "member_id": member_id}),
        ("med_notifications",    {"family_group_id": fgid, "member_id": member_id}),
        ("refill_notifications", {"family_group_id": fgid, "member_id": member_id}),
    ):
        try:
            res = await db[coll].delete_many(query)
            cascade_counts[coll] = res.deleted_count
        except Exception as e:
            logger.warning(f"cascade delete failed for {coll} member={member_id}: {e}")
            cascade_counts[coll] = 0
    logger.info(
        f"Member deleted with cascade: member_id={member_id} family_group={fgid} "
        f"counts={cascade_counts}"
    )
    return {"ok": True, "cascaded": cascade_counts}


@api_router.put("/members/{member_id}/location", response_model=FamilyMember)
async def update_member_location(member_id: str, data: LocationUpdate, current=Depends(get_current_user)):
    # ============================================================
    #  Defense-in-depth: only the member's OWNER device can write
    #  its location.  A stale or buggy client (e.g. v6.7 ↓ which
    #  blindly PUT to members[0]) cannot silently corrupt another
    #  family member's coordinates anymore — the server rejects
    #  with 403 instead of mutating the wrong row.
    #
    #  Allowed callers:
    #    1. The member itself  (current.id == target.user_id)
    #    2. The family-group OWNER (caregiver acting on behalf —
    #       e.g. correcting a senior's location manually). Owners
    #       are a small, trusted population by design.
    #
    #  Targets whose user_id is not yet populated (legacy member
    #  rows from before commit bef9f37 + retro-link script) fall
    #  through to the owner-only path — which is the safer default
    #  until linkage is run.
    # ============================================================
    target = await db.members.find_one(
        {"id": member_id, "family_group_id": current["family_group_id"]},
        {"_id": 0, "id": 1, "user_id": 1},
    )
    if not target:
        raise HTTPException(status_code=404, detail="Member not found")
    is_self = bool(target.get("user_id")) and target["user_id"] == current["id"]
    is_owner = current.get("family_group_role") == "owner"
    if not (is_self or is_owner):
        logger.warning(
            f"location-update REJECTED: user={current['id']} tried to write "
            f"member={member_id} (target.user_id={target.get('user_id')!r}); "
            f"role={current.get('family_group_role')!r}"
        )
        raise HTTPException(
            status_code=403,
            detail="You can only update your own member location.",
        )
    update = {"latitude": data.latitude, "longitude": data.longitude, "last_seen": datetime.now(timezone.utc)}
    if data.location_name:
        update["location_name"] = data.location_name
    r = await db.members.update_one({"id": member_id, "family_group_id": current["family_group_id"]}, {"$set": update})
    if r.matched_count == 0:
        raise HTTPException(status_code=404, detail="Member not found")
    doc = await db.members.find_one({"id": member_id}, {"_id": 0})
    # v1.2.6 diagnostic: every successful location write logs the
    # write-vs-read shape so we can correlate Railway logs against
    # the per-device kc_bg_task_log / kc_location_refresh_log
    # entries.  Specifically — if the device reports an upload-ok
    # with coords (X, Y) but Charles's dashboard still shows stale
    # coords, this log line will tell us whether:
    #   (a) the input we got matches the device's logged send,
    #   (b) the post-write doc actually contains (X, Y), and
    #   (c) which family_group_id and which member.id was touched
    #       (catches "duplicate member doc" silent-drift bugs).
    rd = doc or {}
    logger.info(
        f"loc-write member={member_id} fg={current['family_group_id']} "
        f"user={current['id']} matched={r.matched_count} modified={r.modified_count} "
        f"sent=({data.latitude:.5f},{data.longitude:.5f}) "
        f"stored=({rd.get('latitude')},{rd.get('longitude')}) "
        f"last_seen={rd.get('last_seen')}"
    )
    # v1.3.3 — match this GPS upload to a pending refresh trace
    # so /api/diagnostics/refresh-traces can show end-to-end latency.
    try:
        _trace_record_gps(member_id, data.latitude, data.longitude)
    except Exception as _e:
        pass
    return FamilyMember(**doc)


ALLOWED_CHECKIN_INTERVALS = {2, 4, 6, 8, 12}


@api_router.put("/members/{member_id}/checkin-settings", response_model=FamilyMember)
async def update_checkin_settings(member_id: str, data: CheckinSettings, current=Depends(get_current_user)):
    """Set the daily check-in mode for a member.

    Three valid shapes:
      1. {daily_checkin_time:"HH:MM"}                   — fixed daily time
      2. {checkin_interval_hours: 2|4|6|8|12}           — interval mode
      3. {daily_checkin_time: null, checkin_interval_hours: null}  — disable

    The two modes are mutually exclusive — setting one implicitly clears the other.
    """
    fixed = data.daily_checkin_time
    interval = data.checkin_interval_hours

    if fixed and interval:
        raise HTTPException(
            status_code=400,
            detail="Set either daily_checkin_time OR checkin_interval_hours, not both.",
        )
    if fixed and parse_hhmm(fixed) is None:
        raise HTTPException(status_code=400, detail="daily_checkin_time must be HH:MM format")
    if interval is not None and interval not in ALLOWED_CHECKIN_INTERVALS:
        raise HTTPException(
            status_code=400,
            detail=f"checkin_interval_hours must be one of {sorted(ALLOWED_CHECKIN_INTERVALS)}",
        )

    update: dict = {
        "daily_checkin_time": fixed,
        "checkin_interval_hours": interval,
        "checkin_interval_started_at": (
            datetime.now(timezone.utc) if interval else None
        ),
    }

    r = await db.members.update_one(
        {"id": member_id, "family_group_id": current["family_group_id"]},
        {"$set": update},
    )
    if r.matched_count == 0:
        raise HTTPException(status_code=404, detail="Member not found")
    doc = await db.members.find_one({"id": member_id}, {"_id": 0})
    return FamilyMember(**doc)


