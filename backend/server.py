@api_router.post("/billing/webhook")
async def billing_webhook(request: Request):
    """Stripe webhook handler. Verifies signature when STRIPE_WEBHOOK_SECRET is set."""
    if not billing.is_configured():
        return {"status": "ignored", "reason": "not configured"}
    payload = await request.body()
    sig = request.headers.get("stripe-signature")
    secret = os.environ.get("STRIPE_WEBHOOK_SECRET", "").strip()
    try:
        if secret:
            event = stripe.Webhook.construct_event(payload, sig, secret)
        else:
            event = json.loads(payload)
    except ValueError as e:
        logger.error(f"webhook invalid payload: {e}")
        raise HTTPException(status_code=400, detail="Invalid payload")
    except stripe.error.SignatureVerificationError as e:
        logger.error(f"webhook signature failed: {e}")
        raise HTTPException(status_code=400, detail="Invalid signature")

    etype = event.get("type") if isinstance(event, dict) else getattr(event, "type", None)
    obj = (event.get("data") or {}).get("object") if isinstance(event, dict) else event["data"]["object"]
    # Normalize Stripe StripeObject → plain dict so the rest of this handler can
    # use the dict `.get()` API uniformly. StripeObject doesn't expose `.get`
    # as a method (raises AttributeError) which previously broke live webhook
    # processing for invoice.paid / subscription.* events.
    if obj is not None and not isinstance(obj, dict):
        # Normalize Stripe StripeObject → plain dict. Try public `.to_dict()`
        # first, then private recursive helper, then a plain dict cast as a
        # last resort. The StripeObject's __getattr__ raises AttributeError on
        # missing attrs (including `.get`), which would otherwise break the
        # subsequent `.get("customer")` calls in this handler.
        try:
            obj = obj.to_dict()
        except Exception:
            try:
                obj = obj._to_dict_recursive()
            except Exception:
                try:
                    obj = dict(obj)
                except Exception:
                    logger.error("webhook obj could not be normalized to dict")
                    return {"status": "error", "message": "unrecognized payload shape"}
    logger.info(f"stripe webhook: {etype}")
    try:
        if etype == "checkout.session.completed":
            customer_id = obj.get("customer")
            subscription_id = obj.get("subscription")
            meta = obj.get("metadata") or {}
            # Prefer new key, fall back to legacy for older sessions.
            user_id = meta.get("kinnship_user_id") or meta.get("kinnect_user_id")
            if subscription_id and customer_id:
                sub = stripe.Subscription.retrieve(subscription_id)
                # Resolve user_id from customer if metadata missing
                if not user_id:
                    u = await db.users.find_one(
                        {"subscription.stripe_customer_id": customer_id}, {"_id": 0, "id": 1}
                    )
                    user_id = u and u.get("id")
                if user_id:
                    await billing.apply_subscription_to_user(db, user_id, customer_id, sub)
        elif etype in ("customer.subscription.updated", "customer.subscription.created"):
            customer_id = obj.get("customer")
            meta = obj.get("metadata") or {}
            user_id = meta.get("kinnship_user_id") or meta.get("kinnect_user_id")
            if not user_id and customer_id:
                u = await db.users.find_one(
                    {"subscription.stripe_customer_id": customer_id}, {"_id": 0, "id": 1}
                )
                user_id = u and u.get("id")
            if user_id and customer_id:
                await billing.apply_subscription_to_user(db, user_id, customer_id, obj)
        elif etype == "customer.subscription.deleted":
            customer_id = obj.get("customer")
            if customer_id:
                await billing.revert_user_to_free_by_customer(db, customer_id)
    except Exception as e:
        logger.exception(f"webhook handler failed: {e}")
        return {"status": "error", "message": str(e)}
    return {"status": "ok"}


