@api_router.post("/auth/verify-otp", response_model=TokenResponse)
async def verify_otp(data: OtpVerify):
    """Verify a 6-digit code. On success, return a fresh JWT — and if
    the code was issued for signup, create the user record + family
    group at the same time.
    """
    email = data.email.lower().strip()
    code = (data.code or "").strip()
    if not (code.isdigit() and len(code) == OTP_LENGTH):
        raise HTTPException(status_code=400, detail="Invalid code")

    rec = await db.otp_codes.find_one({"email": email})
    if not rec:
        raise HTTPException(status_code=400, detail="Invalid or expired code")

    # Expiry
    exp = rec.get("expires_at")
    if exp and exp.tzinfo is None:
        exp = exp.replace(tzinfo=timezone.utc)
    if not exp or exp < datetime.now(timezone.utc):
        await db.otp_codes.delete_one({"_id": rec["_id"]})
        raise HTTPException(status_code=400, detail="Invalid or expired code")

    # Attempt throttle
    attempts = int(rec.get("attempts", 0))
    if attempts >= OTP_MAX_ATTEMPTS:
        await db.otp_codes.delete_one({"_id": rec["_id"]})
        raise HTTPException(
            status_code=429,
            detail="Too many attempts. Please request a new code.",
        )

    if not verify_password(code, rec.get("code_hash", "")):
        await db.otp_codes.update_one({"_id": rec["_id"]}, {"$inc": {"attempts": 1}})
        remaining = max(0, OTP_MAX_ATTEMPTS - attempts - 1)
        raise HTTPException(
            status_code=400,
            detail=f"Invalid code. {remaining} attempt(s) remaining.",
        )

    # Code matches — consume it.
    await db.otp_codes.delete_one({"_id": rec["_id"]})

    purpose = rec.get("purpose", "login")
    user = await db.users.find_one({"email": email})

    # === SIGNUP path: create the user + family group on the fly. ===
    if purpose == "signup" and not user:
        tz = rec.get("timezone") or "UTC"
        try:
            ZoneInfo(tz)
        except ZoneInfoNotFoundError:
            tz = "UTC"

        # Resolve invite code (if any) BEFORE creating the user.
        # Accepts EITHER a per-recipient INV-XXXXXX token (single-use,
        # 7-day expiry, tracked in db.family_invites) OR the legacy
        # family-wide KINN-XXXXXX wall code.  The resolver returns the
        # matched group plus the invite row (for INV- tokens only) so
        # we can mark it accepted after the user is created.
        target_group = None
        accepted_invite = None
        invite_code = rec.get("invite_code")
        if invite_code:
            target_group, accepted_invite = await fg.resolve_invite_code(db, invite_code)
            if not target_group:
                raise HTTPException(status_code=404, detail="Invite code not found")

        user_id = str(uuid.uuid4())
        doc = {
            "id": user_id,
            "email": email,
            "full_name": (rec.get("full_name") or "Friend").strip(),
            # No password stored — this field stays absent on
            # passwordless accounts. The presence check is now done
            # purely against `email` + OTP-verification.
            "timezone": tz,
            "push_tokens": [],
            "created_at": datetime.now(timezone.utc),
        }
        await db.users.insert_one(doc)
        if target_group:
            await db.users.update_one(
                {"id": user_id},
                {"$set": {
                    "family_group_id": target_group["id"],
                    "family_group_role": "member",
                }},
            )
            doc["family_group_id"] = target_group["id"]
            doc["family_group_role"] = "member"
            # === Member-record auto-linkage ===
            # When a caregiver pre-created a member row (e.g. "Joyce, 78")
            # and later invited that person by email, the freshly-signed-up
            # user account is still NOT linked to the member row — the
            # `members.user_id` field stays null, blocking self-targeted
            # pushes (e.g. "you have a med to take in 5 min" landing on
            # the SENIOR's phone, not the caregiver's).
            #
            # Heuristic: within the family_group, find an UNLINKED member
            # (user_id is null/missing) whose `name` matches the joining
            # user's full_name (case-insensitive, trimmed).  If exactly
            # one matches, bind them.  Multiple matches → leave unlinked
            # and log (manual intervention needed; safer than guessing).
            try:
                name_target = (doc.get("full_name") or "").strip().lower()
                if name_target:
                    matches = await db.members.find(
                        {
                            "family_group_id": target_group["id"],
                            "$or": [
                                {"user_id": None},
                                {"user_id": {"$exists": False}},
                            ],
                        },
                        {"_id": 0, "id": 1, "name": 1},
                    ).to_list(50)
                    candidates = [
                        m for m in matches
                        if (m.get("name") or "").strip().lower() == name_target
                    ]
                    if len(candidates) == 1:
                        await db.members.update_one(
                            {"id": candidates[0]["id"]},
                            {"$set": {"user_id": user_id}},
                        )
                        logger.info(
                            f"member-link: bound member={candidates[0]['id']} "
                            f"({candidates[0].get('name')}) → user={user_id}"
                        )
                    elif len(candidates) > 1:
                        logger.warning(
                            f"member-link: AMBIGUOUS — {len(candidates)} unlinked "
                            f"members named {doc.get('full_name')!r} in group "
                            f"{target_group['id']}; left unlinked for manual review"
                        )
            except Exception as e:
                logger.warning(f"member-link auto-bind skipped: {e}")
            # If they used a per-recipient INV- token, mark it accepted
            # and notify the inviter via push.  Best-effort — never fail
            # signup just because the bookkeeping push misfires.
            if accepted_invite:
                try:
                    await fg.accept_invite(db, accepted_invite["id"], user_id)
                except Exception as e:
                    logger.warning(f"accept_invite bookkeeping failed: {e}")
                inviter_id = accepted_invite.get("invited_by_user_id")
                if inviter_id and inviter_id != user_id:
                    try:
                        await push_to_user(
                            inviter_id,
                            "✅ Family invite accepted",
                            f"{doc['full_name']} just joined your Kinnship family.",
                            {
                                "type": "family_join",
                                "user_id": user_id,
                                "invite_id": accepted_invite["id"],
                            },
                        )
                    except Exception as e:
                        logger.warning(f"inviter push failed: {e}")
        else:
            group = await fg.create_group_for_user(db, doc)
            doc["family_group_id"] = group["id"]
            doc["family_group_role"] = "owner"
            await seed_demo_data(user_id, group["id"])
        user = doc

    if not user:
        # purpose=login but the user doesn't exist (or signup race) —
        # surface the same vague error as a bad code so we don't leak
        # email-registration status.
        raise HTTPException(status_code=400, detail="Invalid or expired code")

    await fg.ensure_family_group(db, user)
    return TokenResponse(
        access_token=create_access_token(user["id"]),
        user=_user_response(user),
    )


@api_router.post("/auth/me", response_model=UserResponse, include_in_schema=False)
@api_router.get("/auth/me", response_model=UserResponse)
async def me(
    response: Response,
    creds: HTTPAuthorizationCredentials = Depends(security),
    current=Depends(get_current_user),
):
    """Return the current user.

    Side-effect: ROLLING SESSION REFRESH. If the bearer token has
    less than TOKEN_REFRESH_THRESHOLD_DAYS (90) of lifetime left,
    we mint a fresh 1-year token and surface it via:
      • the `X-Refresh-Token` HTTP response header
      • a `refreshed_token` field in the JSON body (added below)
    The frontend AuthContext silently swaps the new token into
    SecureStore. Net effect: any user who opens the app at least
    once every 90 days keeps their session indefinitely. A user
    who is locked out only if they go more than 1 year without
    opening the app, or they tap "Sign out" intentionally.
    """
    try:
        payload = jwt.decode(creds.credentials, SECRET_KEY, algorithms=[ALGORITHM])
        exp_ts = int(payload.get("exp", 0))
        remaining_s = exp_ts - int(datetime.now(timezone.utc).timestamp())
        if remaining_s < TOKEN_REFRESH_THRESHOLD_DAYS * 86400:
            fresh = create_access_token(current["id"])
            # Header AND body field, so the client can pick either
            # one up. Modern axios on RN exposes headers on every
            # response so either works; we send both for safety.
            response.headers["X-Refresh-Token"] = fresh
            body = _user_response(current).model_dump()
            body["refreshed_token"] = fresh
            return body
    except jwt.PyJWTError:
        # If we can't even decode our own token, skip the refresh
        # bookkeeping — get_current_user would have already 401'd
        # if the token were invalid, so this branch is essentially
        # unreachable. Belt-and-suspenders.
        pass
    return _user_response(current)


