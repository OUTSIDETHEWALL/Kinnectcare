@api_router.post("/auth/push-token")
async def register_push_token(data: PushTokenRegister, current=Depends(get_current_user)):
    """Register a push token for the current user.

    A push token uniquely identifies a PHYSICAL DEVICE/install pair.  If the
    same device previously logged into a different account (or was a test
    account), that earlier account still has this token in its push_tokens
    array — which causes "ghost notifications": notifications for someone
    else's family group landing on this device.

    To prevent that, we FIRST pull the token from EVERY OTHER user's
    push_tokens array, THEN set the current user's array to JUST this
    one token.  One token = one user, and one user = one active device.

    ── Why wholesale replace (not $addToSet)? ──
    Expo ROTATES tokens whenever the app is reinstalled, the dev/prod
    signature changes, or certain Android FCM-registration events fire.
    Over time `$addToSet` accumulated DIFFERENT tokens (Charles had 28,
    Joyce had 5) — each one a "ghost" install that FCM still happily
    delivered to.  Result: a single push fanned out 28×, causing the
    notification floods.  Replacing the array with [token] on every
    registration is the v6.7+ fix.  Side effect: only ONE device per
    user account at a time; second-device login boots the first.  This
    is the intended product behavior for Kinnship today.
    """
    if not data.token or not data.token.startswith("ExponentPushToken["):
        return {"ok": False, "reason": "invalid token format"}
    token = data.token
    # 1) Remove this token from any OTHER user that has it (ownership transfer).
    detached = await db.users.update_many(
        {"id": {"$ne": current["id"]}, "push_tokens": token},
        {"$pull": {"push_tokens": token}},
    )
    if detached.modified_count:
        logger.info(
            f"push-token: detached token from {detached.modified_count} prior owner(s) "
            f"new_owner={current['id']} token={token[:25]}..."
        )
    # 2) WIPE-AND-SET — current user gets exactly [token].  Idempotent
    # when already correct; collapses N-ghost arrays to single token
    # when the user's been accumulating stale entries.
    prev = await db.users.find_one(
        {"id": current["id"]}, {"_id": 0, "push_tokens": 1}
    )
    prev_count = len(prev.get("push_tokens") or []) if prev else 0
    await db.users.update_one(
        {"id": current["id"]}, {"$set": {"push_tokens": [token]}}
    )
    pruned = max(0, prev_count - 1)
    if pruned:
        logger.info(
            f"push-token: pruned {pruned} stale ghost token(s) for user={current['id']} "
            f"(kept the one just registered)"
        )
    return {
        "ok": True,
        "detached_from": detached.modified_count,
        "pruned_ghost_tokens": pruned,
    }


class DeleteAccountRequest(BaseModel):
    confirm: Optional[str] = None  # Expect "DELETE" for extra safety


@api_router.delete("/auth/account")
async def delete_account(
    data: DeleteAccountRequest = None,
    current=Depends(get_current_user),
):
    """Permanently delete the authenticated user and ALL of their data.

    Steps:
      1. Cancel active Stripe subscription (best-effort).
      2. Delete all user-owned collections.
      3. Delete the user document.
    """
    user_id = current["id"]
    payload = data or DeleteAccountRequest()
    if (payload.confirm or "").strip().upper() != "DELETE":
        raise HTTPException(
            status_code=400,
            detail='Confirmation required. Send {"confirm":"DELETE"} to proceed.',
        )

    sub = (current.get("subscription") or {})
    sub_id = sub.get("stripe_subscription_id")
    customer_id = sub.get("stripe_customer_id")
    stripe_canceled = False
    customer_deleted = False
    if billing.is_configured():
        # Best-effort cancellation. Do not block account deletion if Stripe fails.
        try:
            if sub_id:
                stripe.Subscription.delete(sub_id)
                stripe_canceled = True
        except Exception as e:
            logger.warning(f"Stripe subscription cancel failed for {user_id}: {e}")
        try:
            if customer_id:
                stripe.Customer.delete(customer_id)
                customer_deleted = True
        except Exception as e:
            logger.warning(f"Stripe customer delete failed for {user_id}: {e}")

    # Delete all user-owned data.
    deleted_counts = {}
    for coll in [
        "members",
        "reminders",
        "checkins",
        "alerts",
        "medication_logs",
    ]:
        try:
            r = await db[coll].delete_many({"owner_id": user_id})
            deleted_counts[coll] = r.deleted_count
        except Exception as e:
            logger.warning(f"Failed to delete from {coll} for {user_id}: {e}")
            deleted_counts[coll] = 0

    # Finally delete the user.
    await db.users.delete_one({"id": user_id})

    logger.info(
        f"Account deleted: user={user_id} stripe_sub_canceled={stripe_canceled} "
        f"stripe_customer_deleted={customer_deleted} counts={deleted_counts}"
    )
    return {
        "ok": True,
        "deleted": deleted_counts,
        "stripe_subscription_canceled": stripe_canceled,
        "stripe_customer_deleted": customer_deleted,
    }


