@api_router.post("/sos")
async def trigger_sos(data: SOSRequest, current=Depends(get_current_user)):
    # Get triggering user's name (the person who pressed SOS) for the push notification.
    triggered_by_name = current.get("full_name") or "A family member"

    # SOS member_id resolution:
    #  - If frontend passed member_id, use that member's name.
    #  - Otherwise the triggering user IS the person in distress; use their name.
    member_name = triggered_by_name
    member_id = data.member_id or current["id"]
    if data.member_id:
        m = await db.members.find_one({"id": data.member_id, "family_group_id": current["family_group_id"]}, {"_id": 0})
        if m:
            member_name = m["name"]
    tz = user_tz(current)
    now_utc = datetime.now(timezone.utc)
    now_local = now_utc.astimezone(tz)
    timestamp_iso = now_utc.isoformat()
    # 12-hour AM/PM with the user's tz abbreviation (e.g. "2:10 PM EDT, May 21").
    # %I gives a zero-padded 12-hour value which we strip to look natural.
    local_time_str = now_local.strftime('%I:%M %p %Z, %b %d').lstrip('0')

    has_coords = data.latitude is not None and data.longitude is not None
    coord_str = ""
    coord_line = ""
    if has_coords:
        coord_str = f" Location: {data.latitude:.4f}, {data.longitude:.4f}."
        coord_line = f"📍 {data.latitude:.5f}, {data.longitude:.5f}"
    else:
        coord_line = "📍 Location unavailable"

    alert = Alert(
        owner_id=current["id"], family_group_id=current["family_group_id"], member_id=member_id, member_name=member_name,
        type="sos", severity="critical",
        title=f"SOS Emergency — {member_name}",
        message=f"{member_name} triggered SOS at {local_time_str}.{coord_str} Emergency services notified.",
        latitude=data.latitude, longitude=data.longitude,
    )
    await db.alerts.insert_one(alert.model_dump())

    # Enhanced push: fan out to ALL users in the family group (every linked
    # family member receives the alert on their own devices).
    fall_prefix = "Fall detected · " if data.fall_detected else ""
    push_title = f"🆘 {fall_prefix}SOS — {member_name}"
    triggered_by_line = (
        f"Triggered by {triggered_by_name}\n" if triggered_by_name and triggered_by_name != member_name else ""
    )
    push_body = f"{triggered_by_line}{coord_line}\n🕒 {local_time_str}\nTap to view & respond."
    push_data = {
        "type": "sos",
        "alert_id": alert.id,
        "member_id": member_id,
        "member_name": member_name,
        "triggered_by_user_id": current["id"],
        "triggered_by_name": triggered_by_name,
        "family_group_id": current["family_group_id"],
        "latitude": data.latitude,
        "longitude": data.longitude,
        "timestamp": timestamp_iso,
        "fall_detected": bool(data.fall_detected),
        "channelId": "sos",
    }
    # ===== SOS BACKGROUND FANOUT =====
    #
    # CRITICAL: push fanout and SMS dispatch were previously awaited inline,
    # which meant if the client disconnected mid-request the work could be
    # cut off (Bug 5 — "SOS background fanout occasionally lags under rapid
    # testing").
    #
    # We now schedule fanout as a FIRE-AND-FORGET asyncio task wrapped in
    # `asyncio.shield()` so it can NEVER be cancelled by the parent request
    # being torn down.  We respond to the client immediately (sub-100ms)
    # while push/SMS continues in the background.
    async def _sos_fanout():
        try:
            # Exclude the triggering user — they're the one calling 911 and
            # are mid-dialer-transition. Receiving their own SOS push during
            # this window can race the Linking.openURL intent on Android,
            # causing the modal to close without the dialer opening
            # (Bug 2 in v6.4 — 15/20 reliability).
            await push_to_family_group(
                current["family_group_id"],
                push_title,
                push_body,
                push_data,
                exclude_user_id=current["id"],
            )
        except Exception as e:
            logger.warning(f"sos push fanout failed: {e}")

        # SMS fanout to designated emergency contacts.
        try:
            ec_cursor = db.members.find(
                {"family_group_id": current["family_group_id"]},
                {"_id": 0, "emergency_contact_phone": 1, "phone": 1, "id": 1, "name": 1},
            )
            ec_docs = await ec_cursor.to_list(500)
            ec_normalized: list = []
            seen_norm: set = set()
            for d in ec_docs:
                raw = d.get("emergency_contact_phone")
                if not raw:
                    continue
                n = sms.normalize_e164(raw)
                if n and n not in seen_norm:
                    seen_norm.add(n)
                    ec_normalized.append(n)
            if has_coords:
                loc_str = f"{data.latitude:.5f}, {data.longitude:.5f}"
            else:
                loc_str = "GPS unavailable"
            sms_body = (
                f"🆘 KINNSHIP ALERT: {member_name} has triggered an emergency SOS. "
                f"Last known location: {loc_str}. "
                "Please check on them immediately or call 911."
            )
            sms_results = await sms.send_sms_to_many(ec_normalized, sms_body)
            sms_sent_bg = sum(1 for r in sms_results if r.get("ok"))
            sms_failed_bg = sum(1 for r in sms_results if not r.get("ok"))
            if sms_results:
                logger.info(
                    f"SOS SMS fanout (bg) — mode={sms.mode()} "
                    f"sent={sms_sent_bg} failed={sms_failed_bg} "
                    f"contacts={len(ec_normalized)}"
                )
        except Exception as e:
            logger.warning(f"sos SMS fanout failed: {e}")

    # Schedule the fanout and IMMEDIATELY return.  We don't await the task,
    # so it continues running in the event loop independently of the request
    # lifecycle.  Strong ref via _BG_TASKS prevents GC.
    task = asyncio.create_task(_sos_fanout())
    _BG_TASKS.add(task)
    task.add_done_callback(_BG_TASKS.discard)

    return {
        "ok": True,
        "alert_id": alert.id,
        "emergency_number": "911",
        "timestamp": timestamp_iso,
        "member_name": member_name,
        "triggered_by_name": triggered_by_name,
        "family_group_id": current["family_group_id"],
        "coordinates": (
            {"latitude": data.latitude, "longitude": data.longitude} if has_coords else None
        ),
        # Fanout is now async-fire-and-forget; counts are reported via logs.
        "fanout_mode": "background",
        "fall_detected": bool(data.fall_detected),
        "sms_mode": sms.mode(),  # "live" or "mock"
    }


